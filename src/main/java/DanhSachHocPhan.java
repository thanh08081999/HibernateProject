    private void themDuLieu() {
        List<HocPhan> dshp = HocPhanDAO.layDanhSachHocPhan();
        
        dshp.forEach(hp -> {
            MonHoc mh = hp.getMaMonHoc();
            GiaoVien gv = hp.getGiaoVien();
            IDHocPhan idhp = hp.getIdHocPhan();
            defaultTableModel.addRow(new Object[]{mh.getMaMonHoc(), mh.getTenMonHoc(),
            mh.getSoTinChi(), gv.getTenGiaoVien(), idhp.getPhongHoc(), idhp.getIdThu(),
            idhp.getIdCa(), hp.getSoSlot()});
        });
    }

