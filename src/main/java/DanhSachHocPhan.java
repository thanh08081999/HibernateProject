    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int r = tbHocPhan.getSelectedRow();
        if (r == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn học phần muốn xóa", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(DanhSachHocPhan.this, "Bạn có chắc muốn xóa học phần này không?");
            if (confirm == JOptionPane.YES_OPTION) {
                IDHocPhan idhp = new IDHocPhan(String.valueOf(tbHocPhan.getValueAt(r, 4)), 
                        String.valueOf(tbHocPhan.getValueAt(r, 5)), String.valueOf(tbHocPhan.getValueAt(r, 6)));
                HocPhanDAO.xoaHocPhan(idhp);
                JOptionPane.showMessageDialog(this, "Xóa học phần thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                defaultTableModel.setRowCount(0);
                themDuLieu();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

