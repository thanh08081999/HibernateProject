    private void btnResetMKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetMKActionPerformed
        int r = tbSinhVien.getSelectedRow();
        if (r == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sinh viên muốn reset mật khẩu", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(QuanLySinhVien.this, "Bạn có chắc muốn reset mật khẩu không?");
            if (confirm == JOptionPane.YES_OPTION) {
                TaiKhoan tksv = TaiKhoanDAO.layThongTinTaiKhoan(String.valueOf(tbSinhVien.getValueAt(r, 0)));
                tksv.setMatKhau(String.valueOf(tbSinhVien.getValueAt(r, 0)));
                TaiKhoanDAO.capNhatThongTinTaiKhoan(tksv);
                JOptionPane.showMessageDialog(this, "Reset mật khẩu thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnResetMKActionPerformed

    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        SinhVien sv = SinhVienDAO.layThongTinSinhVien(txfTimKiem.getText());
        defaultTableModel.setRowCount(0);
        if (sv != null) {
            LopHoc lh = LopHocDAO.layThongTinLopHoc(tenLop);
            defaultTableModel.addRow(new Object[]{sv.getMaSinhVien(), sv.getTenSinhVien(), 
            sv.getGioiTinh(), sv.getNgaySinh(), lh.getTenLop()});
        }
        
    }//GEN-LAST:event_btnTimActionPerformed

