    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int r = tbSinhVien.getSelectedRow();
        if (r == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sinh viên muốn xóa", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(QuanLySinhVien.this, "Bạn có chắc muốn xóa sinh viên này không?");
            if (confirm == JOptionPane.YES_OPTION) {
                String maSV = (String) tbSinhVien.getValueAt(r, 0);
                SinhVien sv = SinhVienDAO.layThongTinSinhVien(maSV);
                LopHoc lop = LopHocDAO.layThongTinLopHoc(tenLop);
                lop.setTongSinhVien(lop.getTongSinhVien() - 1);
                if ("Nam".equals(sv.getGioiTinh()))
                    lop.setTongNam(lop.getTongNam() - 1);
                else lop.setTongNu(lop.getTongNu() - 1);
                if (SinhVienDAO.xoaSinhVien(maSV)) {
                    TaiKhoanDAO.xoaTaiKhoan(maSV);
                    LopHocDAO.capNhatThongTinLopHoc(lop);
                    JOptionPane.showMessageDialog(this, "Xóa sinh viên thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    defaultTableModel.setRowCount(0);
                    themDuLieu();
                }
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

