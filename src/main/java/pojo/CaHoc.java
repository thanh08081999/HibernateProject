package pojo;

public class CaHoc {
    private String idCa;
    private String gioHoc;

    public CaHoc() {
    }

    public CaHoc(String idCa, String gioHoc) {
        this.idCa = idCa;
        this.gioHoc = gioHoc;
    }

    public String getIdCa() {
        return idCa;
    }

    public void setIdCa(String idCa) {
        this.idCa = idCa;
    }

    public String getGioHoc() {
        return gioHoc;
    }

    public void setGioHoc(String gioHoc) {
        this.gioHoc = gioHoc;
    }
    
    
}